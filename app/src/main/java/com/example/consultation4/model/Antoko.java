package com.example.consultation4.model;

public class Antoko {
    private long numParti;
    private String libelleParti;
    private String nomParti;
    private String imgNom;

    public long getNumParti() {
        return numParti;
    }

    public void setNumParti(long numParti) {
        this.numParti = numParti;
    }

    public String getLibelleParti() {
        return libelleParti;
    }

    public void setLibelleParti(String libelleParti) {
        this.libelleParti = libelleParti;
    }

    public String getNomParti() {
        return nomParti;
    }

    public void setNomParti(String nomParti) {
        this.nomParti = nomParti;
    }

    public String getImgNom() {
        return imgNom;
    }

    public void setImgNom(String imgNom) {
        this.imgNom = imgNom;
    }
}
